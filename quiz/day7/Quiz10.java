package com.capgemini.quiz.day7;

public class Quiz10 {
	public static void main(String[] args) {
		String str = "welcome to htd program";
		// this will returns the first character of the string
		char ch1 = str.charAt(0);

		// this will returns the 6th character of the string
		char ch2 = str.charAt(5);

		// this will returns the 12th character of the string
		char ch4 = str.charAt(11);

		// this will returns the 21st character of the string
		char ch3 = str.charAt(20);

		System.out.println("character at 0 index is: " + ch1);
		System.out.println("character at 5th index is: " + ch2);
		System.out.println("character at 11th index is: " + ch3);
		System.out.println("character at 20th index is: " + ch4);

	}

}
